/// The method body generated by a derive generator.
///
/// In 0.2.13 all three derivable classes use BEAM structural primitives, so
/// the "body" is captured as a symbolic tag that the lowering pass translates
/// into Core Erlang.  The tag carries enough information for the lowering pass
/// to emit the right BEAM expression without re-examining the type.
///
/// # Security note â€” `DerivedEq`
///
/// The generated `eq` implementation uses `=:=` (BEAM structural equality),
/// which is **not** constant-time. Types that carry secret data (tokens, hashes,
/// HMACs, session keys) must not derive `Eq`; use `Crypto.constantTimeEq`
/// instead. The `@secret` attribute (planned for a later cut) will make this
/// a hard error automatically.
#[derive(Debug, Clone)]
pub enum DerivedMethodBody {
    /// `eq (a: T) (b: T) -> Bool = (a =:= b)` â€” BEAM structural equality.
    DerivedEq,
    /// `toText` body for a record type. Fields rendered in declared order,
    /// each dispatched through the `toText` method of its type.
    DerivedToTextRecord {
        /// Field names in declaration order.
        field_names: Vec<String>,
        /// `TyConId` of each field's type, in the same order as `field_names`.
        /// Used by the lowering pass to select the correct `toText` dispatch
        /// (e.g. `std.int.toText` for `Int` fields).  `None` when the field
        /// type could not be resolved to a concrete `TyConId` (parametric or
        /// unknown types); the lowering pass emits an identity in that case.
        field_tycons: Vec<Option<TyConId>>,
    },
    /// `toText` body for a union type. Each variant maps to a string; payload
    /// fields are dispatched through `toText`.
    DerivedToTextUnion {
        /// `(ctor_name, payload_field_count, payload_tycons)` in declaration order.
        /// `payload_tycons` carries the `TyConId` for each positional payload
        /// field (same indexing as `payload_field_count`); `None` entries are
        /// treated as identity by the lowering pass.
        variants: Vec<(String, usize, Vec<Option<TyConId>>)>,
    },
    /// `compare` body for a record type. Fields compared in declared order,
    /// first non-`Equal` wins.
    DerivedOrdRecord {
        /// Field names in declaration order.
        field_names: Vec<String>,
    },
    /// `compare` body for a union type. Variants ordered by declaration index;
    /// payloads compared field-by-field when the variant matches.
    DerivedOrdUnion {
        /// `(ctor_name, payload_field_count)` in declaration order.
        variants: Vec<(String, usize)>,
    },

    /// `encode` body for a record type.  Encodes to a `JObject` whose keys are
    /// the field names (binary `Text`) and whose values are structurally-encoded
    /// fields in declaration order.
    DerivedEncodeRecord {
        /// Field names in declaration order.
        field_names: Vec<String>,
        /// Structural encoding shape for each field, in the same order as
        /// `field_names`.  Drives recursive `encode_shape` emission in the
        /// lowering pass.
        field_shapes: Vec<FieldShape>,
    },

    /// `encode` body for a union type.  Nullary variants encode to `JText
    /// "CtorName"`; payload variants encode to an adjacently-tagged object
    /// `{"tag": JText "Ctor", "values": JList [encode_shape(arg)â€¦]}`.
    DerivedEncodeUnion {
        /// `(ctor_name, payload_shapes)` in declaration order.
        /// An empty `payload_shapes` vec signals a nullary variant.
        variants: Vec<(String, Vec<FieldShape>)>,
    },

    /// `decode` body for a record type.  Expects a `JObject`; reads each
    /// declared field via `Map.get`, decodes it according to its shape, and
    /// assembles `Ok(T { f1 = v1, â€¦ })`.  Missing field or wrong JSON kind
    /// short-circuits to `Err`.
    DerivedDecodeRecord {
        /// Field names in declaration order.
        field_names: Vec<String>,
        /// Structural shape for each field, same order as `field_names`.
        /// Drives recursive `decode_shape` emission in the lowering pass.
        field_shapes: Vec<FieldShape>,
    },

    /// `decode` body for a union type.  Dispatches on the JSON shape:
    /// `JText s` â†’ nullary ctor lookup; `JObject m` â†’ payload ctor via
    /// `"tag"`/`"values"` keys.  Unknown tag or bad arity â†’ `Err`.
    DerivedDecodeUnion {
        /// `(ctor_name, payload_shapes)` in declaration order.
        /// An empty `payload_shapes` vec signals a nullary variant.
        variants: Vec<(String, Vec<FieldShape>)>,
    },

    /// `fromRow` body for a record type (`deriving (Row)`).  Reads each field
    /// from its snake-cased column in a `Map Text SqlValue` and decodes it
    /// through that field type's `SqlType.fromSql`, threading the first `Err`
    /// outward (fail-fast), then assembles `Ok(T { f1 = v1, â€¦ })`.  A missing
    /// column short-circuits to `Err`, except for an `Option` field, which reads
    /// a missing or NULL column as `None`.
    DerivedRowRecord {
        /// Record field names in declaration order.
        field_names: Vec<String>,
        /// Snake-cased column name read for each field, same order as
        /// `field_names`.
        columns: Vec<String>,
        /// `SqlType` instance type name used to dispatch `fromSql` for each
        /// field (`"Int"`, `"Text"`, `"Bool"`, `"Float"`), same order. For an
        /// `Option T` field this is the inner `T`; the `Option` wrapping is
        /// recorded in `optionals`.
        field_type_names: Vec<String>,
        /// Whether each field is `Option`-wrapped, same order as `field_names`.
        /// An optional field decodes a NULL or missing column to `None` and a
        /// present value to `Some`, rather than failing.
        optionals: Vec<bool>,
    },

    /// `schemaOf` body for a record type (`deriving (Schema)`).  Returns the
    /// entity's `EntitySchema`, built from the record fields by the data-layer
    /// convention: snake-cased columns, base-type `DbType`s, `Option` fields
    /// nullable, and a field named `id` taken as the primary key (an integer
    /// `id` also marked identity-generated).  A hand-written `HasSchema` instance
    /// spells out the deltas the convention cannot infer.
    DerivedSchemaRecord {
        /// The entity name the schema carries (`schema <name> <table>`).
        entity_name: String,
        /// The SQL table name â€” the snake-cased plural of the entity name.
        table: String,
        /// One spec per record field, in declaration order.
        columns: Vec<SchemaColumnSpec>,
    },

    /// Transparent delegation for an `opaque` single-field wrapper (a newtype).
    ///
    /// Each class method forwards to the inner type's instance: arguments of the
    /// wrapper type are unwrapped (the single field is projected out) on the way
    /// in, and a result of the wrapper type is rewrapped on the way out. The
    /// effect is that `opaque type Money = { cents: Int } deriving (SqlType, â€¦)`
    /// behaves exactly like its inner `Int` for every derived class â€” the wrapper
    /// is indistinguishable from its payload at runtime.
    DerivedDelegated {
        /// The single record field projected to reach the inner value.
        field_name: String,
        /// `TyConId` of the inner type â€” selects its prelude/stdlib dictionary.
        inner_tycon: TyConId,
        /// Source name of the inner type (e.g. `"Int"`), used to build the
        /// `$inst_{Class}_{Inner}` reference for stdlib-class instances.
        inner_type_name: String,
        /// One entry per class method, describing how the wrapper threads
        /// through it (which args to unwrap, whether to rewrap the result).
        methods: Vec<DelegatedMethod>,
    },
}

/// One column of a derived `HasSchema` instance: everything the synthesis needs
/// to emit a `mkColumn â€¦ |> <refinements>` chain for a single record field.
#[derive(Debug, Clone)]
pub struct SchemaColumnSpec {
    /// The record-field name (`mkColumn`'s first argument).
    pub field_name: String,
    /// The SQL column name â€” the snake-cased field (`mkColumn`'s second argument).
    pub column: String,
    /// Whether the column admits NULL (the field is `Option`-wrapped).
    pub nullable: bool,
    /// Whether the column is the table's primary key (the field is named `id`).
    pub primary_key: bool,
    /// The `Generation` constructor name when the column is database-generated
    /// (`Some("Identity")` for an integer `id`); `None` leaves it caller-supplied.
    pub generation: Option<String>,
    /// The `SqlType` instance name used to encode this column in `toInsertRow`
    /// (`"Int"`, `"Text"`, `"Bool"`, `"Float"`), or `None` when the field is not a
    /// base `SqlType` â€” those columns are caller-supplied schema/DDL metadata only,
    /// not part of the insert encoding, so `toInsertRow` leaves them out.
    pub sql_type_tag: Option<String>,
}

/// How the wrapper type flows through one delegated argument position.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum DelegArg {
    /// The argument has the wrapper type â€” unwrap it (project the field) before
    /// passing it to the inner method.
    Wrapped,
    /// The argument does not mention the wrapper type â€” pass it through as-is.
    Plain,
}

/// How the wrapper type flows through a delegated method's result.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum DelegResult {
    /// The result does not mention the wrapper type â€” return the inner result
    /// unchanged (e.g. `toSql` returns a `SqlValue`).
    Plain,
    /// The result is the wrapper type â€” rewrap the inner value.
    Wrap,
    /// The result is `Result wrapper Error` â€” map `Ok n -> Ok (wrap n)` and
    /// forward `Err` unchanged (e.g. `fromSql`/`decode`).
    WrapResult,
}

/// One method of a delegated newtype instance and how the wrapper threads it.
#[derive(Debug, Clone)]
pub struct DelegatedMethod {
    /// Method name (`toSql`, `encode`, â€¦).
    pub name: String,
    /// Per-argument flow, in declaration order.
    pub args: Vec<DelegArg>,
    /// Result flow.
    pub result: DelegResult,
}

/// Structural shape for a single field or payload argument.
///
/// Computed once during derive-time type analysis in `field_to_shape`.  Both
/// the `generate_encode` and `generate_decode` generators share this enum â€”
/// it is purely structural and carries no encode- or decode-specific data.
/// The lowering pass (`ridge-lower`) consumes this to emit the right `IrExpr`
/// without re-examining the AST.
///
/// `TyConId` values carried here use the same fixed assignments as
/// `builtin_name_to_tycon_id`.
#[derive(Debug, Clone, PartialEq, Eq)]
pub enum FieldShape {
    /// A primitive type (`Int=0`, `Float=1`, `Bool=2`, `Text=3`).
    /// Encode: `JInt x` / `JFloat x` / `JBool x` / `JText x`.
    /// Decode: match `JInt n` / â€¦ and return `Ok n`.
    Prim(TyConId),
    /// `JsonValue` itself â€” encode: identity pass-through; decode: `Ok j`.
    Json,
    /// `Option T` â€” encode: `Some x => encode_shape(T, x); None => JNull`.
    /// Decode: `JNull => Ok None; _ => decode_shape(T) |> map Some`.
    Opt(Box<Self>),
    /// `List T` (or the `[T]` sugar).
    /// Encode: `JList(std.list.map (\e -> encode_shape(T, e)) xs)`.
    /// Decode: expect `JList`; fold-decode each element, short-circuit on first `Err`.
    Lst(Box<Self>),
    /// `Map Text T`.
    /// Encode: `JObject(std.map.map (\_k v -> encode_shape(T, v)) m)`.
    /// Decode: expect `JObject`; fold-decode each value, short-circuit on first `Err`.
    MapText(Box<Self>),
    /// `Result T E` â€” adjacently-tagged union shape `{"tag":"Ok"|"Err","values":[â€¦]}`.
    Res(Box<Self>, Box<Self>),
    /// A user-defined type with its own derived or explicit `Encode`/`Decode`
    /// instance.  Lowered to `call Encode__{TypeName}__encode(v)` or
    /// `call Decode__{TypeName}__decode(j)`.
    User {
        /// `TyConId` of the nested type (same arena slot used at definition site).
        tycon: TyConId,
        /// Source-level name of the nested type (e.g. `"Address"`).
        /// Used by the lowering pass to reconstruct the derived function name
        /// `Encode__{type_name}__encode` / `Decode__{type_name}__decode`
        /// without re-examining the AST.
        type_name: String,
    },
    /// A type variable of the generic type being derived (the `a` in
    /// `type Box a = { val: a } deriving (Encode)`).
    ///
    /// The derived instance becomes constrained â€” it takes one runtime
    /// dictionary parameter per used type variable. This shape carries the
    /// variable's parameter index, which selects the matching `$dict_{Class}_{i}`
    /// parameter. The lowering pass projects the class method from that
    /// dictionary: `(maps:get('encode', $dict_Encode_i))(value)`.
    Var {
        /// Zero-based index of the type variable in the type's parameter list.
        /// Doubles as the dictionary parameter index `$dict_{Class}_{param_index}`.
        param_index: usize,
    },
}

/// One generated instance produced by [`derive_instances`].
#[derive(Debug, Clone)]
pub struct DerivedInstance {
    /// The `(ClassId, TyConId)` key this instance covers.
    pub key: (ridge_types::ClassId, TyConId),
    /// The [`InstanceInfo`] registered into [`InstanceEnv`].
    pub instance_info: InstanceInfo,
    /// The synthesised method body (one per instance â€” each derived class has
    /// exactly one method in 0.2.13).
    pub method_body: DerivedMethodBody,
}

// â”€â”€ Public entry point â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€

/// Synthesise instances for every class in `type_decl.deriving` and register
/// them into `instance_env`.
///
/// Returns a list of successfully generated [`DerivedInstance`]s and a list of
/// [`TypeError`]s for any class that could not be derived (e.g. unknown class,
/// non-derivable class, `Float` field in `Eq`).
///
/// The `tycon_id` argument is the [`TyConId`] already assigned to this type
/// in the workspace arena. The `module_id` is used as the `def_module` of the
/// generated instance (the instance lives in the same module as the type).
///
/// `user_tycon_names` maps type names to their [`TyConId`]s; used to check
/// whether a field type is `Float` or another concrete type.
#[expect(
    clippy::implicit_hasher,
    reason = "FxHashMap is the canonical hasher for this crate; matches the pattern in solve.rs and collect.rs"
)]
#[expect(
    clippy::too_many_lines,
    reason = "flat per-class dispatch over the deriving clause (delegated + structural paths); splitting would not reduce complexity"
)]
pub fn derive_instances(
    type_decl: &ridge_ast::decl::TypeDecl,
    tycon_id: TyConId,
    module_id: u32,
    class_table: &ClassTable,
    instance_env: &mut InstanceEnv,
    user_tycon_names: &FxHashMap<String, TyConId>,
) -> (Vec<DerivedInstance>, Vec<TypeError>) {
    let mut generated: Vec<DerivedInstance> = Vec::new();
    let mut errors: Vec<TypeError> = Vec::new();

    // An `opaque` wrapper around a single concrete field is a newtype: its
    // derived instances delegate to the inner type's instances (transparent
    // wrapper semantics). Recognised once; consulted per derived class below.
    let newtype = if type_decl.opaque {
        recognize_newtype(&type_decl.body, user_tycon_names)
    } else {
        None
    };

    for class_ident in &type_decl.deriving {
        let class_name = &class_ident.text;
        let span = class_ident.span;

        // `Table` is a data-layer codegen directive, not a typeclass: it
        // generates a top-level column mirror, synthesized in `tycon_collect`.
        // Skip it here so the class lookup below does not flag it as unknown.
        if class_name == ridge_ast::column_mirror::TABLE_DERIVE {
            continue;
        }

        // `Schema` is the data-layer schema-descriptor derive: like `Row`, it
        // synthesizes a typeclass instance â€” `HasSchema`, whose single method
        // `schemaOf` returns the entity's `EntitySchema` built from the record
        // fields by convention. The derive name (`Schema`) and the class name
        // (`HasSchema`) differ, so it is handled here, before the lookup that
        // resolves a class by its clause spelling, and the class is resolved by
        // `SCHEMA_CLASS` instead.
        if class_name == ridge_ast::column_mirror::SCHEMA_DERIVE {
            let Some(has_schema_id) =
                class_table.id_by_name(ridge_ast::column_mirror::SCHEMA_CLASS)
            else {
                // `HasSchema` lives in std.schema; its absence is an internal
                // wiring fault, surfaced as an unknown-class diagnostic.
                errors.push(TypeError::NoInstance {
                    class: ridge_ast::column_mirror::SCHEMA_CLASS.to_string(),
                    ty: type_decl.name.text.clone(),
                    span,
                    fix_hint: "the `HasSchema` class is unavailable; `deriving \
                               (Schema)` needs `std.schema` in scope"
                        .to_string(),
                });
                continue;
            };
            match generate_schema(&type_decl.body, &type_decl.name, span) {
                Err(e) => errors.push(e),
                Ok(body) => {
                    let info = InstanceInfo {
                        def_module: Some(module_id),
                        methods: vec![
                            ("schemaOf".to_string(), String::new()),
                            ("toInsertRow".to_string(), String::new()),
                        ],
                        ctx_constraints: vec![],
                        head_var_positions: vec![],
                        origin: InstanceOrigin::Explicit,
                        span,
                    };
                    let key = (has_schema_id, tycon_id);
                    let type_name = type_decl.name.text.clone();
                    match instance_env.insert(
                        key,
                        info.clone(),
                        ridge_ast::column_mirror::SCHEMA_CLASS,
                        &type_name,
                    ) {
                        Ok(()) => generated.push(DerivedInstance {
                            key,
                            instance_info: info,
                            method_body: body,
                        }),
                        Err(coherence_err) => errors.push(coherence_err.into_type_error()),
                    }
                }
            }
            continue;
        }

        // Reject unknown class.
        let Some(class_id) = class_table.id_by_name(class_name) else {
            errors.push(TypeError::NoInstance {
                class: class_name.clone(),
                ty: type_decl.name.text.clone(),
                span,
                fix_hint: format!("class `{class_name}` is not defined; check the class name"),
            });
            continue;
        };

        // Transparent newtype delegation. For an `opaque` single-field wrapper,
        // a class with a delegation plan forwards to the inner type's instance
        // instead of synthesising a structural body. `Eq`/`Ord` have no plan and
        // fall through to the structural path â€” on a single field that is already
        // transparent, so they need no special handling.
        if let Some(nt) = &newtype {
            if let Some(plan) = delegation_plan_for_class(class_name) {
                // The inner type must implement the class for delegation to make
                // sense; otherwise the wrapper has nothing to forward to.
                if instance_env.get((class_id, nt.inner_tycon)).is_none() {
                    errors.push(TypeError::NoInstance {
                        class: class_name.clone(),
                        ty: type_decl.name.text.clone(),
                        span,
                        fix_hint: format!(
                            "`deriving {class_name}` on `{}` forwards to its inner type \
                             `{}`, which has no `{class_name}` instance; add one or drop \
                             `{class_name}` from the deriving clause",
                            type_decl.name.text, nt.inner_type_name
                        ),
                    });
                    continue;
                }

                let info = InstanceInfo {
                    def_module: Some(module_id),
                    methods: plan
                        .iter()
                        .map(|m| (m.name.clone(), String::new()))
                        .collect(),
                    ctx_constraints: vec![],
                    head_var_positions: vec![],
                    origin: InstanceOrigin::Explicit,
                    span,
                };
                let key = (class_id, tycon_id);
                let type_name = type_decl.name.text.clone();
                match instance_env.insert(key, info.clone(), class_name, &type_name) {
                    Ok(()) => generated.push(DerivedInstance {
                        key,
                        instance_info: info,
                        method_body: DerivedMethodBody::DerivedDelegated {
                            field_name: nt.field_name.clone(),
                            inner_tycon: nt.inner_tycon,
                            inner_type_name: nt.inner_type_name.clone(),
                            methods: plan,
                        },
                    }),
                    Err(coherence_err) => errors.push(coherence_err.into_type_error()),
                }
                continue;
            }
        }

        // `Row` is the data-layer row decoder class (declared in std.sql). It is
        // structurally derivable for records â€” `fromRow` reads each column via
        // the field type's `SqlType.fromSql` â€” but is not one of the reserved
        // prelude classes, so it gets its own branch ahead of the `is_derivable`
        // gate below (which only admits the prelude-reserved classes).
        if class_name == ridge_ast::column_mirror::ROW_DERIVE {
            match generate_row(&type_decl.body, &type_decl.name, span) {
                Err(e) => errors.push(e),
                Ok(body) => {
                    // `Row` is a three-method class: `fromRow` decodes a row,
                    // `toRow` encodes one, and `rowColumns` names the columns. The
                    // derived instance provides all three, built from the same field
                    // layout (see `generate_row`).
                    let info = InstanceInfo {
                        def_module: Some(module_id),
                        methods: vec![
                            ("fromRow".to_string(), String::new()),
                            ("toRow".to_string(), String::new()),
                            ("rowColumns".to_string(), String::new()),
                        ],
                        ctx_constraints: vec![],
                        head_var_positions: vec![],
                        origin: InstanceOrigin::Explicit,
                        span,
                    };
                    let key = (class_id, tycon_id);
                    let type_name = type_decl.name.text.clone();
                    match instance_env.insert(key, info.clone(), class_name, &type_name) {
                        Ok(()) => generated.push(DerivedInstance {
                            key,
                            instance_info: info,
                            method_body: body,
                        }),
                        Err(coherence_err) => errors.push(coherence_err.into_type_error()),
                    }
                }
            }
            continue;
        }

        // Reject non-derivable classes.
        if !is_derivable(class_id) {
            errors.push(TypeError::NoInstance {
                class: class_name.clone(),
                ty: type_decl.name.text.clone(),
                span,
                fix_hint: format!(
                    "class `{class_name}` is not derivable; \
                     only `Eq`, `ToText` (alias `Show`), `Ord`, `Encode`, and `Decode` support `deriving`"
                ),
            });
            continue;
        }

        // Generate method body, ctx_constraints, and head_var_positions for this
        // class. A generic type whose fields mention its own parameters yields a
        // constrained instance (`instance Encode (Box a) where Encode a`); the
        // head_var_positions tell the solver which head argument carries each
        // element dictionary, mirroring the prelude parametric instances.
        let type_params: Vec<String> = type_decl.params.iter().map(|p| p.text.clone()).collect();
        match generate_body(
            class_id,
            &type_decl.body,
            &type_decl.name,
            span,
            user_tycon_names,
            &type_params,
        ) {
            Err(e) => errors.push(e),
            Ok((body, ctx_constraints, head_var_positions)) => {
                let method_name = method_name_for(class_id);
                let info = InstanceInfo {
                    def_module: Some(module_id),
                    methods: vec![(method_name.to_string(), String::new())],
                    ctx_constraints,
                    head_var_positions,
                    origin: InstanceOrigin::Explicit,
                    span,
                };
                let key = (class_id, tycon_id);
                let type_name = type_decl.name.text.clone();

                match instance_env.insert(key, info.clone(), class_name, &type_name) {
                    Ok(()) => {
                        generated.push(DerivedInstance {
                            key,
                            instance_info: info,
                            method_body: body,
                        });
                    }
                    Err(coherence_err) => {
                        errors.push(coherence_err.into_type_error());
                    }
                }
            }
        }
    }

    (generated, errors)
}

// â”€â”€ Per-class generators â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€

/// Returns `true` if `class_id` is in the 0.3.0 derivable set
/// (`Eq`, `ToText`, `Ord`, `Encode`, `Decode`).
fn is_derivable(class_id: ridge_types::ClassId) -> bool {
    class_id == EQ_CLASS
        || class_id == TOTEXT_CLASS
        || class_id == ORD_CLASS
        || class_id == ENCODE_CLASS
        || class_id == DECODE_CLASS
}

/// The inner field of a recognised newtype: the single record field a
/// transparent wrapper delegates through.
struct NewtypeInfo {
    /// Name of the single record field.
    field_name: String,
    /// `TyConId` of the field's (concrete) type.
    inner_tycon: TyConId,
    /// Source name of the field's type (`"Int"`, `"Text"`, a user type, â€¦).
    inner_type_name: String,
}

/// Recognise a newtype: a record with exactly one field whose type resolves to
/// a concrete `TyConId`. Returns `None` for multi-field records, unions,
/// aliases, or a field whose type is a variable (a generic wrapper like
/// `Box a = { val: a }` keeps the structural/parametric derive path).
///
/// Phantom type parameters are fine â€” `Id a = { raw: Int }` is a newtype over
/// `Int`; the unused `a` does not affect delegation.
fn recognize_newtype(
    body: &TypeBody,
    user_tycon_names: &FxHashMap<String, TyConId>,
) -> Option<NewtypeInfo> {
    let TypeBody::Record(r) = body else {
        return None;
    };
    let [field] = r.fields.as_slice() else {
        return None;
    };
    let inner_tycon = resolve_ast_type_to_tycon_id(&field.ty, user_tycon_names)?;
    let inner_type_name = ast_type_simple_name(&field.ty)?;
    Some(NewtypeInfo {
        field_name: field.name.text.clone(),
        inner_tycon,
        inner_type_name,
    })
}

/// The source-level name of a simple (primitive or named) AST type. Returns
/// `None` for compound types â€” they cannot be the inner type of a newtype.
fn ast_type_simple_name(ty: &AstType) -> Option<String> {
    use ridge_ast::base::PrimitiveType;
    match ty {
        AstType::Primitive { name, .. } => Some(
            match name {
                PrimitiveType::Int => "Int",
                PrimitiveType::Float => "Float",
                PrimitiveType::Bool => "Bool",
                PrimitiveType::Text => "Text",
                PrimitiveType::Unit => "Unit",
                PrimitiveType::Timestamp => "Timestamp",
                PrimitiveType::Decimal => "Decimal",
                PrimitiveType::Uuid => "Uuid",
                PrimitiveType::Bytes => "Bytes",
            }
            .to_string(),
        ),
        AstType::Named { name, .. } => Some(name.text.clone()),
        _ => None,
    }
}

/// The delegation plan for a derivable class: one [`DelegatedMethod`] per class
/// method, describing how the wrapper threads through it.
///
/// Returns `None` for classes that have no delegated form. `Eq`/`Ord` are
/// intentionally absent â€” on a single-field wrapper the structural derive is
/// already transparent (whole-value `=:=` / single-field comparison), so they
/// keep the structural path. User-defined classes are not yet newtype-derivable.
fn delegation_plan_for_class(class_name: &str) -> Option<Vec<DelegatedMethod>> {
    let m = |name: &str, args: Vec<DelegArg>, result: DelegResult| DelegatedMethod {
        name: name.to_string(),
        args,
        result,
    };
    match class_name {
        "ToText" => Some(vec![m(
            "toText",
            vec![DelegArg::Wrapped],
            DelegResult::Plain,
        )]),
        "Encode" => Some(vec![m(
            "encode",
            vec![DelegArg::Wrapped],
            DelegResult::Plain,
        )]),
        "Decode" => Some(vec![m(
            "decode",
            vec![DelegArg::Plain],
            DelegResult::WrapResult,
        )]),
        "SqlType" => Some(vec![
            m("toSql", vec![DelegArg::Wrapped], DelegResult::Plain),
            m("fromSql", vec![DelegArg::Plain], DelegResult::WrapResult),
        ]),
        _ => None,
    }
}

/// Returns the single method name for a derivable class.
fn method_name_for(class_id: ridge_types::ClassId) -> &'static str {
    if class_id == EQ_CLASS {
        "eq"
    } else if class_id == TOTEXT_CLASS {
        "toText"
    } else if class_id == ENCODE_CLASS {
        "encode"
    } else if class_id == DECODE_CLASS {
        "decode"
    } else {
        // ORD_CLASS
        "compare"
    }
}

/// Generate the [`DerivedMethodBody`] and `ctx_constraints` for one class
/// applied to one type body.
///
/// Returns `Err(TypeError)` when the class cannot be derived for this type
/// (e.g. `Float` field in `Eq`).
fn generate_body(
    class_id: ridge_types::ClassId,
    body: &TypeBody,
    type_name: &Ident,
    span: Span,
    user_tycon_names: &FxHashMap<String, TyConId>,
    type_params: &[String],
) -> Result<(DerivedMethodBody, Vec<Constraint>, Vec<usize>), TypeError> {
    if class_id == EQ_CLASS {
        let (b, c) = generate_eq(body, type_name, span, user_tycon_names)?;
        Ok((b, c, vec![]))
    } else if class_id == TOTEXT_CLASS {
        let (b, c) = generate_to_text(body);
        Ok((b, c, vec![]))
    } else if class_id == ENCODE_CLASS {
        generate_encode(
            body,
            type_name,
            span,
            user_tycon_names,
            type_params,
            class_id,
        )
    } else if class_id == DECODE_CLASS {
        generate_decode(
            body,
            type_name,
            span,
            user_tycon_names,
            type_params,
            class_id,
        )
    } else {
        // ORD_CLASS
        let (b, c) = generate_ord(body);
        Ok((b, c, vec![]))
    }
}

/// Collect the distinct type-variable parameter indices used across a set of
/// field shapes, in ascending order. Each becomes one runtime dictionary
/// parameter on a constrained derived instance.
fn collect_var_param_indices(shapes: &[FieldShape]) -> Vec<usize> {
    let mut indices: Vec<usize> = Vec::new();
    for s in shapes {
        collect_var_indices_into(s, &mut indices);
    }
    indices.sort_unstable();
    indices.dedup();
    indices
}

/// Recursively gather `Var` parameter indices from one shape.
fn collect_var_indices_into(shape: &FieldShape, out: &mut Vec<usize>) {
    match shape {
        FieldShape::Var { param_index } => out.push(*param_index),
        FieldShape::Opt(inner) | FieldShape::Lst(inner) | FieldShape::MapText(inner) => {
            collect_var_indices_into(inner, out);
        }
        FieldShape::Res(a, b) => {
            collect_var_indices_into(a, out);
            collect_var_indices_into(b, out);
        }
        FieldShape::Prim(_) | FieldShape::Json | FieldShape::User { .. } => {}
    }
}

/// Build the `(ctx_constraints, head_var_positions)` for a constrained derived
/// instance from the set of used type-variable parameter indices.
///
/// Each used parameter `i` contributes a `class i`-style constraint (the
/// derived class on that variable) and records `i` as the head argument
/// position the runtime dictionary covers. Returns empty vecs when no type
/// variable is used (a concrete type â€” the instance stays unconstrained).
fn var_indices_to_constraints(
    used: &[usize],
    class_id: ridge_types::ClassId,
) -> (Vec<Constraint>, Vec<usize>) {
    let ctx_constraints = used
        .iter()
        // Sentinel â€” the solver substitutes the concrete arg via
        // head_var_positions, mirroring the prelude parametric instances.
        .map(|_| Constraint::single(class_id, ridge_types::TyVid(0)))
        .collect();
    (ctx_constraints, used.to_vec())
}

// â”€â”€ derive Eq â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€

/// Generate `derive Eq` â€” whole-value BEAM `=:=`.
///
/// Fails with T029 when any field type is `Float` (the float-footgun guard).
fn generate_eq(
    body: &TypeBody,
    type_name: &Ident,
    span: Span,
    user_tycon_names: &FxHashMap<String, TyConId>,
) -> Result<(DerivedMethodBody, Vec<Constraint>), TypeError> {
    // Walk all field types and reject `Float`.
    if let Some(float_field) = find_float_field(body, user_tycon_names) {
        return Err(TypeError::NoInstance {
            class: "Eq".to_string(),
            ty: type_name.text.clone(),
            span,
            fix_hint: format!(
                "floating-point equality is a footgun; `Eq Float` is intentionally \
                 omitted from the prelude. Field `{float_field}` has type `Float`. \
                 Do not derive `Eq` on `Float`-bearing types if equality must be \
                 correct. Use explicit comparison logic or `Crypto.constantTimeEq` \
                 for secrets."
            ),
        });
    }

    // Records (Erlang maps) and unions (tagged tuples / bare atoms) both lower
    // to BEAM values for which `=:=` gives the correct structural equality.
    // No ctx_constraints are needed: `=:=` is monomorphic in BEAM.
    Ok((DerivedMethodBody::DerivedEq, vec![]))
}

/// Walks the type body and returns the name of the first `Float`-typed field,
/// or `None` if no `Float` field is found.
///
/// Checks positional constructor args and record fields. Does not recurse
/// into nested types (a `List Float` field does not trigger this guard â€” the
/// float is wrapped in a container and the Eq instance for `List` already
/// handles structural equality correctly at the BEAM level).
fn find_float_field<'a>(
    body: &'a TypeBody,
    user_tycon_names: &FxHashMap<String, TyConId>,
) -> Option<&'a str> {
    match body {
        TypeBody::Record(r) => {
            for field in &r.fields {
                if ast_type_is_float(&field.ty, user_tycon_names) {
                    return Some(&field.name.text);
                }
            }
            None
        }
        TypeBody::Union(u) => {
            for ctor in &u.alternatives {
                match ctor {
                    Constructor::Positional { args, .. } => {
                        for ty in args {
                            if ast_type_is_float(ty, user_tycon_names) {
                                // Return a synthetic name for the positional payload.
                                return Some("(payload)");
                            }
                        }
                    }
                    Constructor::Record { body, .. } => {
                        for field in &body.fields {
                            if ast_type_is_float(&field.ty, user_tycon_names) {
                                return Some(&field.name.text);
                            }
                        }
                    }
                }
            }
            None
        }
        TypeBody::Alias(ty) => {
            if ast_type_is_float(ty, user_tycon_names) {
                Some("(alias)")
            } else {
                None
            }
        }
    }
}

/// Returns `true` if the AST type is `Float` (either as a primitive or a named
/// type that resolves to the Float `TyConId`).
fn ast_type_is_float(ty: &AstType, user_tycon_names: &FxHashMap<String, TyConId>) -> bool {
    use ridge_ast::base::PrimitiveType;
    match ty {
        AstType::Primitive {
            name: PrimitiveType::Float,
            ..
        } => true,
        AstType::Named { name, .. } => {
            // Resolve via user tycon names; Float is also in the builtin table.
            let id = user_tycon_names
                .get(name.text.as_str())
                .copied()
                .or_else(|| builtin_name_to_tycon_id(&name.text));
            id == Some(TyConId(1)) // Float = TyConId(1)
        }
        _ => false,
    }
}

// â”€â”€ derive ToText â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€

/// Generate `derive ToText`.
///
/// - Records â†’ `"TypeName { f1 = " ++ toText(f1) ++ ", f2 = " ++ toText(f2) ++ " }"`.
///   Each field requires a `ToText` instance for its type; `field_tycons` carries
///   the resolved `TyConId` per field so the lowering pass can select the right
///   stdlib dispatch (e.g. `std.int.toText` for `Int`).
/// - Unions â†’ `"CtorName"` for nullary constructors; `"CtorName(f1, f2)"` with
///   per-payload `toText` dispatch for payload constructors.
fn generate_to_text(body: &TypeBody) -> (DerivedMethodBody, Vec<Constraint>) {
    // ctx_constraints: in 0.2.13 all fields of derivable user types are concrete,
    // so the constraint is resolved statically at derive-registration time.
    // We leave ctx_constraints empty â€” the solver verifies at call sites using
    // the InstanceEnv. The structural synthesis only needs field names + TyConIds.
    match body {
        TypeBody::Record(r) => {
            let field_names: Vec<String> = r.fields.iter().map(|f| f.name.text.clone()).collect();
            let field_tycons: Vec<Option<TyConId>> = r
                .fields
                .iter()
                .map(|f| resolve_ast_type_to_tycon_id(&f.ty, &rustc_hash::FxHashMap::default()))
                .collect();
            (
                DerivedMethodBody::DerivedToTextRecord {
                    field_names,
                    field_tycons,
                },
                vec![],
            )
        }
        TypeBody::Union(u) => {
            let variants = u
                .alternatives
                .iter()
                .map(|ctor| match ctor {
                    Constructor::Positional { name, args, .. } => {
                        let tycons: Vec<Option<TyConId>> = args
                            .iter()
                            .map(|ty| {
                                resolve_ast_type_to_tycon_id(ty, &rustc_hash::FxHashMap::default())
                            })
                            .collect();
                        (name.text.clone(), args.len(), tycons)
                    }
                    Constructor::Record { name, body, .. } => {
                        let tycons: Vec<Option<TyConId>> = body
                            .fields
                            .iter()
                            .map(|f| {
                                resolve_ast_type_to_tycon_id(
                                    &f.ty,
                                    &rustc_hash::FxHashMap::default(),
                                )
                            })
                            .collect();
                        (name.text.clone(), body.fields.len(), tycons)
                    }
                })
                .collect();
            (DerivedMethodBody::DerivedToTextUnion { variants }, vec![])
        }
        TypeBody::Alias(_) => {
            // Alias types are structural wrappers; the ToText of the underlying
            // type is correct, but we cannot derive it without knowing the
            // underlying type's class instance at this point. For 0.2.13 this
            // case is uncommon; represent it as a zero-field record render.
            (
                DerivedMethodBody::DerivedToTextRecord {
                    field_names: vec![],
                    field_tycons: vec![],
                },
                vec![],
            )
        }
    }
}

// â”€â”€ derive Ord â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€

/// Generate `derive Ord`.
///
/// - Records â†’ field-by-field in declared order, first non-`Equal` wins.
///   Requires `Ord` per field type (`ctx_constraints`).
/// - Unions â†’ compare variant index first; if same variant and has payload,
///   compare payload fields in order. Requires `Ord` per payload type.
///
/// Returns `Ordering` (`Less | Equal | Greater`). The `Eq` superclass
/// requirement is checked by coherence (T033) â€” not validated here.
fn generate_ord(body: &TypeBody) -> (DerivedMethodBody, Vec<Constraint>) {
    match body {
        TypeBody::Record(r) => {
            let field_names = r.fields.iter().map(|f| f.name.text.clone()).collect();
            (DerivedMethodBody::DerivedOrdRecord { field_names }, vec![])
        }
        TypeBody::Union(u) => {
            let variants = u
                .alternatives
                .iter()
                .map(|ctor| match ctor {
                    Constructor::Positional { name, args, .. } => (name.text.clone(), args.len()),
                    Constructor::Record { name, body, .. } => {
                        (name.text.clone(), body.fields.len())
                    }
                })
                .collect();
            (DerivedMethodBody::DerivedOrdUnion { variants }, vec![])
        }
        TypeBody::Alias(_) => {
            // Delegate to the underlying type's Ord.
            (
                DerivedMethodBody::DerivedOrdRecord {
                    field_names: vec![],
                },
                vec![],
            )
        }
    }
}

// â”€â”€ derive Encode â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€

/// Generate `derive Encode`.
///
/// Records encode to a `JObject` whose keys are the field names (binary `Text`)
/// and values are structurally-encoded fields in declaration order.
/// Union variants encode as follows:
/// - Nullary â†’ `JText "CtorName"` (bare JSON string â€” the DX-idiomatic form).
/// - Payload â†’ `{"tag": JText "Ctor", "values": JList [encode_shape(arg)â€¦]}`
///   (adjacently-tagged object; round-trips cleanly with derived Decode).
///
/// Returns `Err(TypeError::NoInstance/T029)` when any field or payload type
/// IS or CONTAINS a type variable of the instance (generic user types such as
/// `type Box a = { val: a } deriving (Encode)` need parametric instances,
/// planned for a later cut).
///
/// Threads the REAL `user_tycon_names` map (unlike `generate_to_text`, which
/// passes `FxHashMap::default()` and only resolves builtin head names).
fn generate_encode(
    body: &TypeBody,
    type_name: &Ident,
    span: Span,
    user_tycon_names: &FxHashMap<String, TyConId>,
    type_params: &[String],
    class_id: ridge_types::ClassId,
) -> Result<(DerivedMethodBody, Vec<Constraint>, Vec<usize>), TypeError> {
    let var_hint = |field: &str, ty: &AstType| TypeError::NoInstance {
        class: "Encode".to_string(),
        ty: type_name.text.clone(),
        span,
        fix_hint: format!(
            "field `{field}` of type `{}` mentions a type variable that is not a \
             parameter of `{}`; only the type's own parameters can be threaded \
             through a derived instance",
            ast_type_display(ty),
            type_name.text,
        ),
    };
    match body {
        TypeBody::Record(r) => {
            let mut field_names = Vec::with_capacity(r.fields.len());
            let mut field_shapes = Vec::with_capacity(r.fields.len());
            for field in &r.fields {
                let shape = field_to_shape(&field.ty, user_tycon_names, type_params)
                    .map_err(|()| var_hint(&field.name.text, &field.ty))?;
                field_names.push(field.name.text.clone());
                field_shapes.push(shape);
            }
            let (cc, hvp) =
                var_indices_to_constraints(&collect_var_param_indices(&field_shapes), class_id);
            Ok((
                DerivedMethodBody::DerivedEncodeRecord {
                    field_names,
                    field_shapes,
                },
                cc,
                hvp,
            ))
        }
        TypeBody::Union(u) => {
            let mut variants = Vec::with_capacity(u.alternatives.len());
            let mut all_shapes: Vec<FieldShape> = Vec::new();
            for ctor in &u.alternatives {
                match ctor {
                    Constructor::Positional { name, args, .. } => {
                        let mut shapes = Vec::with_capacity(args.len());
                        for ty in args {
                            let shape = field_to_shape(ty, user_tycon_names, type_params)
                                .map_err(|()| var_hint(&name.text, ty))?;
                            all_shapes.push(shape.clone());
                            shapes.push(shape);
                        }
                        variants.push((name.text.clone(), shapes));
                    }
                    Constructor::Record { name, body, .. } => {
                        let mut shapes = Vec::with_capacity(body.fields.len());
                        for field in &body.fields {
                            let shape = field_to_shape(&field.ty, user_tycon_names, type_params)
                                .map_err(|()| var_hint(&field.name.text, &field.ty))?;
                            all_shapes.push(shape.clone());
                            shapes.push(shape);
                        }
                        variants.push((name.text.clone(), shapes));
                    }
                }
            }
            let (cc, hvp) =
                var_indices_to_constraints(&collect_var_param_indices(&all_shapes), class_id);
            Ok((DerivedMethodBody::DerivedEncodeUnion { variants }, cc, hvp))
        }
        TypeBody::Alias(ty) => {
            // Alias types are structural wrappers; use the shape of the underlying type.
            let shape = field_to_shape(ty, user_tycon_names, type_params)
                .map_err(|()| var_hint("$alias", ty))?;
            let (cc, hvp) = var_indices_to_constraints(
                &collect_var_param_indices(std::slice::from_ref(&shape)),
                class_id,
            );
            // Represent as a single-field record with a synthetic field name.
            Ok((
                DerivedMethodBody::DerivedEncodeRecord {
                    field_names: vec!["$alias".to_string()],
                    field_shapes: vec![shape],
                },
                cc,
                hvp,
            ))
        }
    }
}

// â”€â”€ derive Decode â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€

/// Generate `derive Decode`.
///
/// Records decode from a `JObject`; each field is looked up with `Map.get`,
/// decoded according to its [`FieldShape`], and assembled into `Ok(T { â€¦ })`.
/// A missing key or wrong JSON kind short-circuits to `Err`.
///
/// Union variants dispatch on the JSON shape: `JText s` â†’ nullary ctor;
/// `JObject m` â†’ payload ctor via `"tag"`/`"values"` keys.  Unknown tag or
/// bad arity â†’ `Err`.
///
/// Returns `Err(TypeError::NoInstance/T029)` for the same var-boundary cases
/// as [`generate_encode`] (generic user types, type-variable fields).
fn generate_decode(
    body: &TypeBody,
    type_name: &Ident,
    span: Span,
    user_tycon_names: &FxHashMap<String, TyConId>,
    type_params: &[String],
    class_id: ridge_types::ClassId,
) -> Result<(DerivedMethodBody, Vec<Constraint>, Vec<usize>), TypeError> {
    let var_hint = |field: &str, ty: &AstType| TypeError::NoInstance {
        class: "Decode".to_string(),
        ty: type_name.text.clone(),
        span,
        fix_hint: format!(
            "field `{field}` of type `{}` mentions a type variable that is not a \
             parameter of `{}`; only the type's own parameters can be threaded \
             through a derived instance",
            ast_type_display(ty),
            type_name.text,
        ),
    };
    match body {
        TypeBody::Record(r) => {
            let mut field_names = Vec::with_capacity(r.fields.len());
            let mut field_shapes = Vec::with_capacity(r.fields.len());
            for field in &r.fields {
                let shape = field_to_shape(&field.ty, user_tycon_names, type_params)
                    .map_err(|()| var_hint(&field.name.text, &field.ty))?;
                field_names.push(field.name.text.clone());
                field_shapes.push(shape);
            }
            let (cc, hvp) =
                var_indices_to_constraints(&collect_var_param_indices(&field_shapes), class_id);
            Ok((
                DerivedMethodBody::DerivedDecodeRecord {
                    field_names,
                    field_shapes,
                },
                cc,
                hvp,
            ))
        }
        TypeBody::Union(u) => {
            let mut variants = Vec::with_capacity(u.alternatives.len());
            let mut all_shapes: Vec<FieldShape> = Vec::new();
            for ctor in &u.alternatives {
                match ctor {
                    Constructor::Positional { name, args, .. } => {
                        let mut shapes = Vec::with_capacity(args.len());
                        for ty in args {
                            let shape = field_to_shape(ty, user_tycon_names, type_params)
                                .map_err(|()| var_hint(&name.text, ty))?;
                            all_shapes.push(shape.clone());
                            shapes.push(shape);
                        }
                        variants.push((name.text.clone(), shapes));
                    }
                    Constructor::Record { name, body, .. } => {
                        let mut shapes = Vec::with_capacity(body.fields.len());
                        for field in &body.fields {
                            let shape = field_to_shape(&field.ty, user_tycon_names, type_params)
                                .map_err(|()| var_hint(&field.name.text, &field.ty))?;
                            all_shapes.push(shape.clone());
                            shapes.push(shape);
                        }
                        variants.push((name.text.clone(), shapes));
                    }
                }
            }
            let (cc, hvp) =
                var_indices_to_constraints(&collect_var_param_indices(&all_shapes), class_id);
            Ok((DerivedMethodBody::DerivedDecodeUnion { variants }, cc, hvp))
        }
        TypeBody::Alias(ty) => {
            // Alias types: decode the underlying shape and wrap in Ok.
            let shape = field_to_shape(ty, user_tycon_names, type_params)
                .map_err(|()| var_hint("$alias", ty))?;
            let (cc, hvp) = var_indices_to_constraints(
                &collect_var_param_indices(std::slice::from_ref(&shape)),
                class_id,
            );
            Ok((
                DerivedMethodBody::DerivedDecodeRecord {
                    field_names: vec!["$alias".to_string()],
                    field_shapes: vec![shape],
                },
                cc,
                hvp,
            ))
        }
    }
}

// â”€â”€ derive Row â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€

/// Generate `derive Row` for a record type.
///
/// Produces a [`DerivedMethodBody::DerivedRowRecord`]: each field reads its
/// snake-cased column from the `Map Text SqlValue` row and decodes it through
/// the field type's `SqlType.fromSql`. Only records are supported (a union or
/// alias has no column layout). A field is either one of the base `SqlType`
/// primitives â€” `Int`, `Text`, `Bool`, or `Float` â€” or an `Option` of one of
/// them, which reads a NULL or missing column as `None`. Anything else (a user
/// type, a container, an `Option` of a non-primitive) yields a `NoInstance`
/// error naming the offending field; richer field types land once their
/// `SqlType` instances exist.
fn generate_row(
    body: &TypeBody,
    type_name: &Ident,
    span: Span,
) -> Result<DerivedMethodBody, TypeError> {
    let TypeBody::Record(r) = body else {
        return Err(TypeError::NoInstance {
            class: "Row".to_string(),
            ty: type_name.text.clone(),
            span,
            fix_hint: "`deriving (Row)` is supported on record types only; a row \
                       maps named columns to record fields"
                .to_string(),
        });
    };
    let mut field_names = Vec::with_capacity(r.fields.len());
    let mut columns = Vec::with_capacity(r.fields.len());
    let mut field_type_names = Vec::with_capacity(r.fields.len());
    let mut optionals = Vec::with_capacity(r.fields.len());
    for field in &r.fields {
        let Some((optional, type_tag)) = sql_row_field(&field.ty) else {
            return Err(TypeError::NoInstance {
                class: "Row".to_string(),
                ty: type_name.text.clone(),
                span,
                fix_hint: format!(
                    "field `{}` of type `{}` has no `SqlType` instance; \
                     `deriving (Row)` currently supports fields of type \
                     Int, Text, Bool, Float, or an Option of one of them",
                    field.name.text,
                    ast_type_display(&field.ty),
                ),
            });
        };
        field_names.push(field.name.text.clone());
        columns.push(ridge_ast::column_mirror::column_sql_name(&field.name.text));
        field_type_names.push(type_tag);
        optionals.push(optional);
    }
    Ok(DerivedMethodBody::DerivedRowRecord {
        field_names,
        columns,
        field_type_names,
        optionals,
    })
}

/// Synthesise the `schemaOf` body for `deriving (Schema)` on a record.
///
/// Walks the record fields and assigns each its conventional column metadata:
/// the snake-cased column name, the base `DbType` for its declared type,
/// nullability from an `Option` wrapper, and â€” for a field named `id` â€” the
/// primary key (plus identity generation when that `id` is an integer, the usual
/// serial key). Unlike `Row`, every field maps to a column regardless of its
/// type: the convention falls back to `DbText` for what it cannot infer, and a
/// hand-written instance states the rest. `deriving (Schema)` on a non-record
/// type is rejected, the way `deriving (Row)` is.
fn generate_schema(
    body: &TypeBody,
    type_name: &Ident,
    span: Span,
) -> Result<DerivedMethodBody, TypeError> {
    use ridge_ast::column_mirror as cm;

    let TypeBody::Record(r) = body else {
        return Err(TypeError::NoInstance {
            class: cm::SCHEMA_CLASS.to_string(),
            ty: type_name.text.clone(),
            span,
            fix_hint: "`deriving (Schema)` is supported on record types only; a \
                       schema maps record fields to table columns"
                .to_string(),
        });
    };

    let columns = r
        .fields
        .iter()
        .map(|field| {
            let is_id = field.name.text == "id";
            let nullable = cm::is_optional_type(&field.ty);
            // A non-null integer `id` is the conventional serial primary key â€”
            // identity-generated so the insert path can fill it. A non-integer or
            // nullable `id` is still the key, but supplied by the caller. The same
            // predicate decides which columns the insert companion drops, so both
            // read it from `column_mirror`.
            let generation = if cm::is_generated_field(&field.name.text, &field.ty) {
                Some("Identity".to_string())
            } else {
                None
            };
            // `toInsertRow` encodes the same way `Row.toRow` does, so the SqlType
            // tag comes from the same field classifier â€” `None` for a field that
            // is not a base `SqlType` (those map to a column for DDL but carry no
            // insert encoding).
            let sql_type_tag = sql_row_field(&field.ty).map(|(_optional, tag)| tag);
            SchemaColumnSpec {
                field_name: field.name.text.clone(),
                column: cm::column_sql_name(&field.name.text),
                nullable,
                primary_key: is_id,
                generation,
                sql_type_tag,
            }
        })
        .collect();

    Ok(DerivedMethodBody::DerivedSchemaRecord {
        entity_name: type_name.text.clone(),
        table: cm::table_sql_name(&type_name.text),
        columns,
    })
}

/// Structurally synthesise a `Row` instance for a record that did not ask for
/// one with `deriving (Row)`.
///
/// `Row` is a structural capability: any record whose fields are all `SqlType`
/// primitives can be turned into a `Map Text SqlValue` and back. Synthesising
/// it on sight lets a plain in-memory `List record` flow through the query
/// verbs (filter/select/orderBy/â€¦) with no annotation, the same way the
/// database path works for entities that `deriving (Row)`.
///
/// Returns `None` â€” leaving the type alone â€” when it
/// - already requested `Row` explicitly (the `deriving` pass owns those),
/// - already has a `Row` instance (hand-written or previously synthesised), or
/// - is not a record, or has a field with no `SqlType` (a user type, a
///   container, a type variable); `generate_row` rejects those.
///
/// The generated instance carries the same shape an explicit `deriving (Row)`
/// would, so both the constraint solver (via `InstanceEnv`) and the lowering
/// (via the returned `DerivedInstance`) treat it identically.
pub(crate) fn synthesize_implicit_row(
    type_decl: &ridge_ast::decl::TypeDecl,
    tycon_id: TyConId,
    module_id: u32,
    class_table: &ClassTable,
    instance_env: &mut InstanceEnv,
) -> Option<DerivedInstance> {
    // The `deriving` pass already handled an explicit request.
    if type_decl
        .deriving
        .iter()
        .any(|d| d.text == ridge_ast::column_mirror::ROW_DERIVE)
    {
        return None;
    }
    let row_class_id = class_table.id_by_name(ridge_ast::column_mirror::ROW_DERIVE)?;
    // Something already covers this key â€” leave it; an explicit instance wins.
    if instance_env.get((row_class_id, tycon_id)).is_some() {
        return None;
    }
    // Non-record or non-primitive field â†’ no implicit Row.
    let body = generate_row(&type_decl.body, &type_decl.name, type_decl.name.span).ok()?;
    let info = InstanceInfo {
        def_module: Some(module_id),
        methods: vec![
            ("fromRow".to_string(), String::new()),
            ("toRow".to_string(), String::new()),
            ("rowColumns".to_string(), String::new()),
        ],
        ctx_constraints: vec![],
        head_var_positions: vec![],
        origin: InstanceOrigin::Explicit,
        span: type_decl.name.span,
    };
    let key = (row_class_id, tycon_id);
    // A coherence clash means an instance we did not see registered first; the
    // existing one wins, so drop the implicit one silently.
    instance_env
        .insert(key, info.clone(), "Row", &type_decl.name.text)
        .ok()?;
    Some(DerivedInstance {
        key,
        instance_info: info,
        method_body: body,
    })
}

/// Classify a `deriving (Row)` field type into `(is_optional, primitive_tag)`.
///
/// A bare primitive (`Int`/`Text`/`Bool`/`Float`) is `(false, tag)`; an
/// `Option P` of a primitive is `(true, inner_tag)`, decoded as a nullable
/// column. Anything else â€” a non-primitive, a container, or an `Option` of a
/// non-primitive â€” returns `None`, which `generate_row` reports as `NoInstance`.
fn sql_row_field(ty: &AstType) -> Option<(bool, String)> {
    match ty {
        AstType::Paren { inner, .. } => sql_row_field(inner),
        AstType::App { head, args, .. } if head.text == "Option" && args.len() == 1 => {
            Some((true, sql_primitive_type_name(&args[0])?))
        }
        _ => sql_primitive_type_name(ty).map(|tag| (false, tag)),
    }
}

/// The `SqlType` instance type name for a field type, when it is one the prelude
/// `SqlType` instances cover: a base primitive, or `JsonValue` (the prelude JSON
/// ADT, which maps to a `json`/`jsonb` column). Returns `None` for any other type
/// (a user type, container, or `Option`), which has no `fromSql` to dispatch yet.
fn sql_primitive_type_name(ty: &AstType) -> Option<String> {
    use ridge_ast::base::PrimitiveType;
    match ty {
        AstType::Primitive { name, .. } => match name {
            PrimitiveType::Int => Some("Int".to_string()),
            PrimitiveType::Text => Some("Text".to_string()),
            PrimitiveType::Bool => Some("Bool".to_string()),
            PrimitiveType::Float => Some("Float".to_string()),
            PrimitiveType::Timestamp => Some("Timestamp".to_string()),
            PrimitiveType::Decimal => Some("Decimal".to_string()),
            PrimitiveType::Uuid => Some("Uuid".to_string()),
            PrimitiveType::Bytes => Some("Bytes".to_string()),
            PrimitiveType::Unit => None,
        },
        AstType::Named { name, .. } => match name.text.as_str() {
            "Int" | "Text" | "Bool" | "Float" | "Decimal" | "Uuid" | "Bytes" | "JsonValue" => {
                Some(name.text.clone())
            }
            _ => None,
        },
        AstType::Paren { inner, .. } => sql_primitive_type_name(inner),
        _ => None,
    }
}

/// Recursively distil an [`AstType`] into a [`FieldShape`].
///
/// Returns `Err(())` when the type IS or CONTAINS a type variable (`Type::Var`)
/// at any position â€” this signals the Gap-A boundary: the field needs a
/// parametric instance that the compiler does not yet support for `deriving`.
///
/// Recognised special cases (DX-idiomatic):
/// - Primitives (`Int`, `Float`, `Bool`, `Text`) â†’ [`FieldShape::Prim`].
/// - `JsonValue` (named) â†’ [`FieldShape::Json`].
/// - `Option T` â†’ [`FieldShape::Opt`] (recurses into `T`).
/// - `List T` or `[T]` sugar â†’ [`FieldShape::Lst`].
/// - `Map Text T` â†’ [`FieldShape::MapText`] (only the `Text`-keyed form).
/// - `Result T E` â†’ [`FieldShape::Res`].
/// - Other user-defined named type â†’ [`FieldShape::User`] (must have its
///   own `Encode`/`Decode` instance; coherence checks this at use-site).
fn field_to_shape(
    ty: &AstType,
    user_tycon_names: &FxHashMap<String, TyConId>,
    type_params: &[String],
) -> Result<FieldShape, ()> {
    use ridge_ast::base::PrimitiveType;
    match ty {
        // â”€â”€ Primitives â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
        AstType::Primitive { name, .. } => {
            let id = match name {
                PrimitiveType::Int => TyConId(0),
                PrimitiveType::Float => TyConId(1),
                PrimitiveType::Bool => TyConId(2),
                PrimitiveType::Text => TyConId(3),
                // Unit and other primitives fall through to identity/Json handling.
                _ => return Ok(FieldShape::Json),
            };
            Ok(FieldShape::Prim(id))
        }

        // â”€â”€ Type variable â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
        // A variable that is one of the generic type's parameters becomes a
        // `Var` shape: the derived instance threads a runtime dictionary for it.
        // A variable that is NOT a declared parameter is genuinely unresolvable
        // (Gap-A boundary) â€” reject so deriving fails with T029.
        AstType::Var { name, .. } => type_params
            .iter()
            .position(|p| p == &name.text)
            .map(|param_index| FieldShape::Var { param_index })
            .ok_or(()),

        // â”€â”€ List sugar [T] â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
        AstType::List { elem, .. } => {
            let inner = field_to_shape(elem, user_tycon_names, type_params)?;
            Ok(FieldShape::Lst(Box::new(inner)))
        }

        // â”€â”€ Parenthesised form â€” unwrap â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
        AstType::Paren { inner, .. } => field_to_shape(inner, user_tycon_names, type_params),

        // â”€â”€ Named type (no args) â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
        AstType::Named { name, .. } => {
            let n = name.text.as_str();
            // JsonValue itself is an identity pass-through.
            if n == "JsonValue" {
                return Ok(FieldShape::Json);
            }
            // Builtin scalars by name.
            if let Some(id) = builtin_name_to_tycon_id(n) {
                // Only Int/Float/Bool/Text are Prim; others are pass-through.
                if id.0 <= 3 {
                    return Ok(FieldShape::Prim(id));
                }
            }
            // User-defined type: look it up in the user tycon map.
            if let Some(&id) = user_tycon_names.get(n) {
                return Ok(FieldShape::User {
                    tycon: id,
                    type_name: n.to_string(),
                });
            }
            // Unknown named type â€” treat as identity (best-effort; coherence
            // will catch missing instances at call sites).
            Ok(FieldShape::Json)
        }

        // â”€â”€ Type application App { head, args } â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
        AstType::App { head, args, .. } => {
            let h = head.text.as_str();
            match h {
                "Option" if args.len() == 1 => {
                    let inner = field_to_shape(&args[0], user_tycon_names, type_params)?;
                    Ok(FieldShape::Opt(Box::new(inner)))
                }
                "List" if args.len() == 1 => {
                    let inner = field_to_shape(&args[0], user_tycon_names, type_params)?;
                    Ok(FieldShape::Lst(Box::new(inner)))
                }
                "Map" if args.len() == 2 => {
                    // Only Map Text T is supported; any other key type falls
                    // through to Json (best-effort â€” user may get a runtime
                    // encoding that isn't fully round-trippable).
                    let key_is_text = matches!(
                        &args[0],
                        AstType::Primitive {
                            name: ridge_ast::base::PrimitiveType::Text,
                            ..
                        }
                    ) || matches!(
                        &args[0],
                        AstType::Named { name, .. } if name.text == "Text"
                    );
                    if key_is_text {
                        let val_shape = field_to_shape(&args[1], user_tycon_names, type_params)?;
                        Ok(FieldShape::MapText(Box::new(val_shape)))
                    } else {
                        // Non-Text keys: encode as opaque Json (no recursion).
                        Ok(FieldShape::Json)
                    }
                }
                "Result" if args.len() == 2 => {
                    let ok_shape = field_to_shape(&args[0], user_tycon_names, type_params)?;
                    let err_shape = field_to_shape(&args[1], user_tycon_names, type_params)?;
                    Ok(FieldShape::Res(Box::new(ok_shape), Box::new(err_shape)))
                }
                _ => {
                    // Generic application â€” reject any arg that contains a type
                    // variable not covered by the type's own parameters (those
                    // would need a nested parametric instance we cannot yet
                    // synthesise). A user type head applied to concrete args is
                    // a plain `User` dispatch.
                    for arg in args {
                        check_no_var(arg)?;
                    }
                    // Known user type (head) applied to concrete args â€” treat as User.
                    if let Some(&id) = user_tycon_names.get(h) {
                        Ok(FieldShape::User {
                            tycon: id,
                            type_name: h.to_string(),
                        })
                    } else {
                        Ok(FieldShape::Json)
                    }
                }
            }
        }

        // â”€â”€ Everything else â€” function types, tuples, inline records â”€â”€â”€â”€â”€â”€â”€â”€â”€
        // These are unusual in derivable type fields; treat as Json identity.
        AstType::Tuple { elems, .. } => {
            // Reject any Var buried inside â€” a tuple of type variables would
            // need nested dictionary threading beyond what deriving synthesises.
            for e in elems {
                check_no_var(e)?;
            }
            Ok(FieldShape::Json)
        }
        AstType::Fn { .. } | AstType::Record { .. } => Ok(FieldShape::Json),
    }
}

/// Returns `Err(())` if `ty` contains a `Type::Var` anywhere in its spine.
///
/// Used for nested positions (a generic-type application argument, a tuple
/// element) where the deriver cannot thread a runtime dictionary. A top-level
/// type-variable field is handled separately by `field_to_shape`'s `Var` arm.
fn check_no_var(ty: &AstType) -> Result<(), ()> {
    match ty {
        AstType::Var { .. } => Err(()),
        AstType::Paren { inner, .. } => check_no_var(inner),
        AstType::List { elem, .. } => check_no_var(elem),
        AstType::App { args, .. } => {
            for a in args {
                check_no_var(a)?;
            }
            Ok(())
        }
        AstType::Tuple { elems, .. } => {
            for e in elems {
                check_no_var(e)?;
            }
            Ok(())
        }
        _ => Ok(()),
    }
}

/// A minimal display string for an AST type, used in error hints.
fn ast_type_display(ty: &AstType) -> String {
    use ridge_ast::base::PrimitiveType;
    match ty {
        AstType::Primitive { name, .. } => match name {
            PrimitiveType::Int => "Int".to_string(),
            PrimitiveType::Float => "Float".to_string(),
            PrimitiveType::Bool => "Bool".to_string(),
            PrimitiveType::Text => "Text".to_string(),
            PrimitiveType::Unit => "Unit".to_string(),
            PrimitiveType::Timestamp => "Timestamp".to_string(),
            PrimitiveType::Decimal => "Decimal".to_string(),
            PrimitiveType::Uuid => "Uuid".to_string(),
            PrimitiveType::Bytes => "Bytes".to_string(),
        },
        AstType::Named { name, .. } | AstType::Var { name, .. } => name.text.clone(),
        AstType::App { head, args, .. } => {
            let args_str: Vec<String> = args.iter().map(ast_type_display).collect();
            format!("{} {}", head.text, args_str.join(" "))
        }
        AstType::List { elem, .. } => format!("[{}]", ast_type_display(elem)),
        AstType::Paren { inner, .. } => format!("({})", ast_type_display(inner)),
        AstType::Tuple { elems, .. } => {
            let s: Vec<String> = elems.iter().map(ast_type_display).collect();
            format!("({})", s.join(", "))
        }
        AstType::Fn { .. } => "<fn>".to_string(),
        AstType::Record { .. } => "{ â€¦ }".to_string(),
    }
}

